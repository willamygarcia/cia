package beans;

public class ViaturaBean {
	
	private Integer codigoViatura;
	private String placaViatura;
	private String marcaViatura;
	private String chassiViatura;
	private String corViatura;
	private String anoViatura;
	public Integer getCodigoViatura() {
		return codigoViatura;
	}
	public void setCodigoViatura(Integer codigoViatura) {
		this.codigoViatura = codigoViatura;
	}
	public String getPlacaViatura() {
		return placaViatura;
	}
	public void setPlacaViatura(String placaViatura) {
		this.placaViatura = placaViatura;
	}
	public String getMarcaViatura() {
		return marcaViatura;
	}
	public void setMarcaViatura(String marcaViatura) {
		this.marcaViatura = marcaViatura;
	}
	public String getChassiViatura() {
		return chassiViatura;
	}
	public void setChassiViatura(String chassiViatura) {
		this.chassiViatura = chassiViatura;
	}
	public String getCorViatura() {
		return corViatura;
	}
	public void setCorViatura(String corViatura) {
		this.corViatura = corViatura;
	}
	public String getAnoViatura() {
		return anoViatura;
	}
	public void setAnoViatura(String anoViatura) {
		this.anoViatura = anoViatura;
	}
	
	

}
