package beans;

public class DocumentosOcorrenciaBean {
	
	private int codigoDocumento;
	private int codigoDocumentoOcorrencia;
	private String descricaoDocumento;
	private String documento;
	
	public int getCodigoDocumento() {
		return codigoDocumento;
	}
	public void setCodigoDocumento(int codigoDocumento) {
		this.codigoDocumento = codigoDocumento;
	}
	public int getCodigoDocumentoOcorrencia() {
		return codigoDocumentoOcorrencia;
	}
	public void setCodigoDocumentoOcorrencia(int codigoDocumentoOcorrencia) {
		this.codigoDocumentoOcorrencia = codigoDocumentoOcorrencia;
	}
	public String getDescricaoDocumento() {
		return descricaoDocumento;
	}
	public void setDescricaoDocumento(String descricaoDocumento) {
		this.descricaoDocumento = descricaoDocumento;
	}
	public String getDocumento() {
		return documento;
	}
	public void setDocumento(String documento) {
		this.documento = documento;
	}
	
	

}
