package beans;

public class ArmasOcorrenciaBean {
	private int codigoArma;
	private int codigoOcorrencia;
	private String serieArma;
	private String tipoArma;
	private String funcionamentoArma;
	private String marcaArma;
	private String calibreArma;
	private String capacidadeArma;
	private String informacoesArama;
	public int getCodigoArma() {
		return codigoArma;
	}
	public void setCodigoArma(int codigoArma) {
		this.codigoArma = codigoArma;
	}
	public int getCodigoOcorrencia() {
		return codigoOcorrencia;
	}
	public void setCodigoOcorrencia(int codigoOcorrencia) {
		this.codigoOcorrencia = codigoOcorrencia;
	}
	public String getSerieArma() {
		return serieArma;
	}
	public void setSerieArma(String serieArma) {
		this.serieArma = serieArma;
	}
	public String getTipoArma() {
		return tipoArma;
	}
	public void setTipoArma(String tipoArma) {
		this.tipoArma = tipoArma;
	}
	public String getFuncionamentoArma() {
		return funcionamentoArma;
	}
	public void setFuncionamentoArma(String funcionamentoArma) {
		this.funcionamentoArma = funcionamentoArma;
	}
	public String getMarcaArma() {
		return marcaArma;
	}
	public void setMarcaArma(String marcaArma) {
		this.marcaArma = marcaArma;
	}
	public String getCalibreArma() {
		return calibreArma;
	}
	public void setCalibreArma(String calibreArma) {
		this.calibreArma = calibreArma;
	}
	public String getCapacidadeArma() {
		return capacidadeArma;
	}
	public void setCapacidadeArma(String capacidadeArma) {
		this.capacidadeArma = capacidadeArma;
	}
	public String getInformacoesArama() {
		return informacoesArama;
	}
	public void setInformacoesArama(String informacoesArama) {
		this.informacoesArama = informacoesArama;
	}
	
	

}
